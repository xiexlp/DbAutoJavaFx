    public static boolean isName(String str) {
        //将str转换为char类型数组
        char[] ch = str.toCharArray();
        boolean bn = false;//假设不包含数字
        boolean bm = false;//假设不包含字母
        //遍历char数组
        for (int i = 0; i < ch.length; i++) {
            if (Character.isDigit(ch[i])) {//判断是否包含数字
                bn = true;//是数字
                break;
            }
        }
        //再次遍历数组 判断是否是字母
        for (int i = 0; i < ch.length; i++) {
            if (Character.isLetter(ch[i])) {//判断是否包含字母
                bm = true;
                break;
            }
        }
        if (bn && bm) {//bn==true&&bm==true
            return true;
        }
        return false;
    }

